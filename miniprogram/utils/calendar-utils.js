// 简易农历数据（示例年份数据，实际生产环境建议使用专业库如 lunar-javascript）
// 这里仅为了演示，使用简单的查表法或算法
// 考虑到小程序体积，这里我们先只实现公历节日和简单的农历转换（或使用 Mock 数据）

function getFestival(year, month, day) {
  const dateStr = `${String(month).padStart(2, '0')}-${String(day).padStart(2, '0')}`;
  return SOLAR_FESTIVALS[dateStr] || '';
}

